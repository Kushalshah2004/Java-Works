class Main
{
	public static void main(String args[])
	{
		//For Binary

		double num = 0b101;
		System.out.println(num);

		num = 0B101;
		System.out.println(num);

		//For Hexadecimal

		num = 0x7e;
		System.out.println(num);

		num = 0x7E;
		System.out.println(num);

		num = 0X7e;
		System.out.println(num);

		num = 0X7E;
		System.out.println(num);
		
	}
}